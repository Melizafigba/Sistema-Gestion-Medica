    private void jinsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jinsertarActionPerformed
    try {
        int idtipo = 0;
//        Date date = jfecha.getDate();
//        long d = date.getTime();
//        Date fecha = new Date(d);  
        ResultSet rs = cone.consultabd("SELECT id FROM tipo_cita WHERE nombre = '"+jtipo.getSelectedItem()+"'");
        while (rs.next()){
            idtipo = rs.getInt(1);
        }
        cone.modificabd("INSERT INTO ordenes_citas(nombre,apellido,cedula,celular,correo,fecha_cita,horario,Jornada,tipo_cita_id,Sexo)"
                + "VALUES ('"+jnombre.getText()+"','"+japellido.getText()+"','"+jcedula.getText()+"','"+jcelular.getText()+"','"
                +jcorreo.getText()+"','"+funcion.getFecha(jfecha)+"','"+jhorario.getText()+"','"+jJornada.getSelectedItem()+"', "+idtipo+",'"+jSexo.getSelectedItem()+"') ");
        JOptionPane.showMessageDialog(null, " El Registro Fue Exitoso ");
    } catch (SQLException ex) {
        Logger.getLogger(PacienteNuevo.class.getName()).log(Level.SEVERE, null, ex);
    }
limpiar();


    }//GEN-LAST:event_jinsertarActionPerformed

